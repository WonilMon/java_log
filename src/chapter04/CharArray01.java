package chapter04;

public class CharArray01 {

	public static void main(String[] args) {
		
		String str [] = {"김유신", "강감찬", "을지문덕", "이순신"};
		String res= "";
		
		for (int i = 0; i<str.length; i++) {
			
			res += "\t" + str[i] + "\n";
			
		}
		
		System.out.println(res);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
