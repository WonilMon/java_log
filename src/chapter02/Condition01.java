package chapter02;

public class Condition01 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 15;
		boolean result;
		
		result = a >= b ? true : false;
		
		System.out.println(result);
		
		System.out.println("------------");
		
		int n1 = 20;
		int n2 = 20;
		char result2;
		char result3;
		
		result2 = (++n1 == n2) ? 'Y' : 'N';
		System.out.println(result2);
		result3 = (n1-- == n2) ? 'Y' : 'N';
		System.out.println(n1);
		System.out.println(result3);
		
		
		System.out.println("------------");
		
		
		
		
		
		
		
		
		
		
		

	}

}
